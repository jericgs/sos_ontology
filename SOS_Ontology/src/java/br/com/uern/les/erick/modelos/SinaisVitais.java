package br.com.uern.les.erick.modelos;

/**
 *
 * @author jerick.gs
 */
public class SinaisVitais {
    
    private int idV;
    private String pa;
    private String fc;
    private String fr;
    private String satSemSuport;
    private String satComSuport;
    private String temperatura;
    private String hgt;
    private String glasgow;

    public int getIdV() {
        return idV;
    }

    public void setIdV(int idV) {
        this.idV = idV;
    }

    public String getPa() {
        return pa;
    }

    public void setPa(String pa) {
        this.pa = pa;
    }

    public String getFc() {
        return fc;
    }

    public void setFc(String fc) {
        this.fc = fc;
    }

    public String getFr() {
        return fr;
    }

    public void setFr(String fr) {
        this.fr = fr;
    }

    public String getSatSemSuport() {
        return satSemSuport;
    }

    public void setSatSemSuport(String satSemSuport) {
        this.satSemSuport = satSemSuport;
    }

    public String getSatComSuport() {
        return satComSuport;
    }

    public void setSatComSuport(String satComSuport) {
        this.satComSuport = satComSuport;
    }

    public String getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }

    public String getHgt() {
        return hgt;
    }

    public void setHgt(String hgt) {
        this.hgt = hgt;
    }

    public String getGlasgow() {
        return glasgow;
    }

    public void setGlasgow(String glasgow) {
        this.glasgow = glasgow;
    }
        
}
